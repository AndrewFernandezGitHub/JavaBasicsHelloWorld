package com.syntax.class02;

public class Homework {

	public static void main(String[] args) {

		// data types to represent whole numbers
		byte pizzas = 127;
		System.out.println(pizzas);
		short salads = 32767;
		int steaks = 327677666;
		long pasta = 4057123456778999l;
		// data types to represent decimals
		float price = 13.99f;
		double price1 = 14.99;
		// to represent a single character
		char type = 'c';
		char payment = '$';
		// to represent yes or no
		boolean dinein = false;
		boolean carryout = true;

		System.out.println(salads);
		System.out.println(steaks);
		System.out.println(pasta);
		System.out.println(price);
		System.out.println(price1);
		System.out.println(type);
		System.out.println(payment);
		System.out.println(dinein);
		System.out.println(carryout);

	}

}
